    private void btnabrirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnabrirActionPerformed
    {//GEN-HEADEREND:event_btnabrirActionPerformed
        if (seleccionado.showDialog(this, "ABRIR ARCHIVO") == JFileChooser.APPROVE_OPTION)
        {
            archivo = seleccionado.getSelectedFile();
            if (archivo.canRead())
            {
                if (archivo.getName().endsWith("txt"))
                {
                    String contenido = gestion.AbrirATexto(archivo);
                    txtAreatexto.setText(contenido);
                } else
                {
                    JOptionPane.showMessageDialog(null, "Archivo no correspondiente");
                }
            }
        }
    }//GEN-LAST:event_btnabrirActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnguardarActionPerformed
    {//GEN-HEADEREND:event_btnguardarActionPerformed
        if (seleccionado.showDialog(this, "GUARDAR") == JFileChooser.APPROVE_OPTION)
        {
            archivo = seleccionado.getSelectedFile();
            if (archivo.getName().endsWith("txt"))
            {
                String contenido = txtAreatexto.getText();
                String respuesta = gestion.GuardarATexto(archivo, contenido);
                if (respuesta != null)
                {
                    JOptionPane.showMessageDialog(jMenu1, respuesta);
                } else
                {
                    JOptionPane.showMessageDialog(null, "Error al guardar texto");
                }
            } else
            {
                JOptionPane.showMessageDialog(null, "El texto debe ser finalizado con txt");
            }
        }

    }//GEN-LAST:event_btnguardarActionPerformed

