    public void calcular2()
    {
        String n1 = txtIngreso.getText();
        String n2 = txtIngreso2.getText();
        String n3 = txtIngreso3.getText();
        String n4 = txtIngreso4.getText();
        String n5 = txtIngreso5.getText();
        String n6 = txtIngreso6.getText();

        float num1 = Float.parseFloat(n1);
        float num2 = Float.parseFloat(n2);
        float num3 = Float.parseFloat(n3);
        float num4 = Float.parseFloat(n4);
        float num5 = Float.parseFloat(n5);
        float num6 = Float.parseFloat(n6);

        float semanal = (num1 + num2 + num3 + num4 + num5 + num6);
        txtIngre.setText(String.valueOf(semanal)); 
        

    }

