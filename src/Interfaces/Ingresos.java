    public void mostrar()
    {
        String matriz2[][] = new String[lista.size()][6];

        for (int i = 0; i < lista.size(); i++)
        {
            matriz2[i][0] = lista.get(i).getIngresoLunes();
            matriz2[i][1] = lista.get(i).getIngresoMartes();
            matriz2[i][2] = lista.get(i).getIngresoMiercoles();
            matriz2[i][3] = lista.get(i).getIngresoJueves();
            matriz2[i][4] = lista.get(i).getIngresoViernes();
            matriz2[i][5] = lista.get(i).getIngresoSabado();

        }
        jTableIngresos.setModel(new javax.swing.table.DefaultTableModel(
                matriz2,
                new String[]
                {
                    "Lunes", "Martes", "Miercoles", "Jueves", "Viernes", "Sabado"
                }
        ));

    }
