    public void mostrar()
    {
        String matriz[][] = new String[lista.size()][9];

        for (int i = 0; i < lista.size(); i++)
        {
            matriz[i][0] = lista.get(i).getNombre();
            matriz[i][1] = lista.get(i).getExistencias();
            matriz[i][2] = lista.get(i).getTamanioCM();
            matriz[i][3] = lista.get(i).getMarca();
            matriz[i][4] = lista.get(i).getPrecio();
            matriz[i][5] = lista.get(i).getColor();
            matriz[i][6] = lista.get(i).getCodigoDeBarras();
            matriz[i][7] = lista.get(i).getUnidades();
            matriz[i][8] = lista.get(i).getUTotales();
        }

        jTableMateriales.setModel(new javax.swing.table.DefaultTableModel(
                matriz,
                new String[]
                {
                    "Nombre del producto", "Numero de excistencias", "Tamaño en Cm", "Marca", "Precio", "Color", "Código de Barras", "Unidades vendidas"
                }
        ));

    }

