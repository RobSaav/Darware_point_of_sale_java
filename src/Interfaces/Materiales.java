    public void calcular()
    {
        //Variables tiempo
        String n3 = txtExcistencias.getText();
        String n4 = txtUnidades.getText();
        //Variables Ingresos
        String n1 = txtPrecio.getText();
        String n2 = txtUnidades.getText();
        //Calculo Ingresos

        float numero1 = Float.parseFloat(n1);
        float numero2 = Float.parseFloat(n2);
        float resultado = numero1 * numero2;
        txtIng.setText(String.valueOf(resultado));
        txtSemana.setText(String.valueOf(resultado));

        //Calculo unidades por día
        float numero3 = Float.parseFloat(n3);
        float numero4 = Float.parseFloat(n4);

        float resultado2 = ((numero3) - (numero4));
        txtTotales.setText(String.valueOf(resultado2));

        float acumulador = 0;
        float total5 = 0;
        for (int i = 0; i < lista2.size(); i++)
        {
            acumulador = acumulador + resultado;
            txtse.setText(String.valueOf(acumulador));
        }

    }

