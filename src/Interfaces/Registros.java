    public void metodo() throws IOException
    {
        {
            int i = 0;

            FileReader myFile = null;
            try
            {
//Leo un Archivo de Texto
                String file = "C:/Users/rober/OneDrive/Escritorio/Materiales.txt";
                myFile = new FileReader(file);
                BufferedReader InputFile = new BufferedReader(myFile);
// Read the first line
                String currentRecord = InputFile.readLine();

                while (currentRecord != null)
                {
                    currentRecord = InputFile.readLine();

                    try
                    {
//Copio un valor a la celda
                        JTableMateriales2.setValueAt(currentRecord, 0, i);
//Refresco la Tabla
                        JTableMateriales2.paintImmediately(JTableMateriales2.getX(), JTableMateriales2.getY(), JTableMateriales2.getWidth(), JTableMateriales2.getHeight());
                        i = i + 1;

                    } catch (Exception ex)
                    {
                    }

                }
            } catch (FileNotFoundException ex)
            {
                Logger.getLogger(Registros.class.getName()).log(Level.SEVERE, null, ex);
            } finally
            {
                try
                {
                    myFile.close();
                } catch (IOException ex)
                {
                    Logger.getLogger(Registros.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

